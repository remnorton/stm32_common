uint32_t pow_table[] = {1,10,100,1000,10000,100000};

int16_t int_sin(int angle)
{
	int a = angle % 360;
	if (a < 0) a = 360 + a;
	return sin_table[a];
}

int16_t int_cos(int angle)
{
	return int_sin(angle+90);
}

int16_t int_tan(int angle)
{
	return int_sin(angle)/int_cos(angle);
}

int16_t int_ctan(int angle)
{
	return int_cos(angle)/int_sin(angle);
}

uint32_t pow_10(int value)
{
	uint32_t res = 1;
	if (value > 5) res *= pow_10(value - 5);
	else res = pow_table[value];
	return res;
}

uint32_t getArcX(uint32_t x, uint16_t angle, uint32_t rad)
{
    return x - ((int32_t)rad)*int_sin(angle)/10000;
}

uint32_t getArcY(uint32_t y, uint16_t angle, uint32_t rad)
{
    return y - ((int32_t)rad)*int_cos(angle)/10000;
}
